public class D1_41Teste {
  public static void main(String[] args) {

    int i = 0;

    // Exemplo de loop do-while que imprime "Roda 1x" 5 vezes

    do {
      System.out.println("Roda 1x");
      i++;
    } while (i < 5);

  }
}

// Neste Exemplo vemos que o i pode ter seu valor alterado dentro do loop,
// Isso faz com que i fique em Loop até o While permitir
// Caso i > 5, o loop não será executado mais
