public class F1_4Teste {
  public static void main(String[] args) {

    //Declaração e inicialização do array em 2D ( matriz 2x2 )

    float[][] matriz = {
        { 1.0f, 2.0f },
        { 3.0f, 4.0f }
    };

    // Acesso e impressão dos elementos da matriz
    System.out.println("Elementos da matriz:");
    System.out.println("Elemento [0][0]: " + matriz[0][0]);
    System.out.println("Elemento [0][1]: " + matriz[0][1]);
    System.out.println("Elemento [1][0]: " + matriz[1][0]);
    System.out.println("Elemento [1][1]: " + matriz[1][1]);
  }
}
