public class A1_1Teste {
  public static void main(String[] args) {
    // Declaração de variáveis inteiras e reais
    byte numeroByte = 5; // Tipo byte
    short numeroShort = 100; // Tipo short
    int numeroInt = 10;      // Tipo inteiro (int)
    long numeroLong = 100000L; // Tipo long ( sufixo L)
    double numeroDouble = 10.5; // Tipo real (double)

    System.out.println("Esse é o numeroByte: " + numeroByte);
    System.out.println("Esse é o numeroShort: " + numeroShort);
    System.out.println("Esse é o numeroInt: " + numeroInt);
    System.out.println("Esse é o numeroLong: " + numeroLong);
    System.out.println("Esse é o numeroDouble: " + numeroDouble);
    // Exibindo o tipo de cada variável
   }
}
